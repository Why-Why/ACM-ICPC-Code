using namespace std;

int N,a[1003];

inline bool judge(int x) {
	int last=x%10;
	x/=10;

	for(;x;x/=10) {
		if(x%10!=last-1) return 0;
		last=x%10;
	}

	return 1;
}

int main() {
	//freopen("in.txt","r",stdin);
	//freopen("out.txt","w",stdout);

	scanf("%d",&N);
	for(int i=1;i<=N;++i) scanf("%d",a+i);

	int ans=-1;
	for(int i=1;i<=N;++i)
		for(int j=1;j<i;++j)
			if(judge(a[i]*a[j])) ans=max(ans,a[i]*a[j]);

	printf("%d\n",ans);

	return 0;
}
