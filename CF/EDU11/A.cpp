using namespace std;

const int MaxN=1005;

int N;
int num[MaxN];
int ans[MaxN<<2],acou;

int gcd(int a,int b) {
	if(!a) return b;
	return gcd(b%a,a);
}

int main() {
	//freopen("in.txt","r",stdin);
	//freopen("out.txt","w",stdout);

	scanf("%d",&N);
	for(int i=1;i<=N;++i) {
		scanf("%d",num+i);
		if(i==1 || gcd(num[i],num[i-1])==1) ans[++acou]=num[i];
		else ans[++acou]=1,ans[++acou]=num[i];
	}

	printf("%d\n",acou-N);
	for(int i=1;i<=acou;++i) printf("%d ",ans[i]);
	puts("");
	
	return 0;
}
