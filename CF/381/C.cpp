using namespace std;

int N,M;

int main() {
	//freopen("in.txt","r",stdin);
	//freopen("out.txt","w",stdout);

	int ans=1000000000;
	int l,r;

	scanf("%d %d",&N,&M);
	while(M--) {
		scanf("%d %d",&l,&r);
		ans=min(ans,r-l+1);
	}

	printf("%d\n",ans);
	for(int i=1,j=0;i<=N;++i,(j+=1)%=ans)
		printf("%d ",j);
	puts("");

	return 0;
}
